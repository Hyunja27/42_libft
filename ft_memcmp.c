#include "libft.h"

int	ft_memcmp(const void *ptr1, const void *ptr2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if ((char*)ptr1 == (char*)ptr2)
			continue;
		else
			return ((char*)ptr1 - (char*)ptr2);
	}
	return (0);
}
